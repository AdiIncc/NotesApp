import Foundation
import UIKit

extension UIColor {
    static var iconColor: UIColor {
        return UIColor(named: "iconColor")!
    }
    
    static var tabBarColor: UIColor {
        return UIColor(named: "tabBarBackground")!
    }
    
    static var unselectedIconColor: UIColor {
        return UIColor(named: "unselectedIconColor")!
    }
}
